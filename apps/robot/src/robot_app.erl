-module(robot_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    time_server:start_link(1000),
    robot_ctl:start_link(),
    robot_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

